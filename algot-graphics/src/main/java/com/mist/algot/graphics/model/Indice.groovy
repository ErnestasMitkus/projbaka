package com.mist.algot.graphics.model

class Indice {
    private final int x, y, z

    Indice(int x, int y, int z) {
        this.x = x
        this.y = y
        this.z = z
    }

    int getX() {
        return x
    }

    int getY() {
        return y
    }

    int getZ() {
        return z
    }
}
